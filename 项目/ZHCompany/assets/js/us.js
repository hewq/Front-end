$(function(){
    /*左边动画*/
    $(".content-left").hover(function(){
        $(this).find(".move").animate({
            "margin-left":"-10px",
            opacity:0.6
        },300,"linear");
        $(this).find(".description").animate({
            bottom:"370px"
        },300,"linear");
        $(this).find(".about").animate({
            bottom:"100px",
            opacity:0.8
        },300,"linear");
    },function(){
        $(this).find(".move").animate({
            "margin-left":"0px",
            opacity:1
        },300,"linear");
        $(this).find(".about").animate({
            bottom:"90px",
            opacity:0
        },300,"linear");
        $(this).find(".description").animate({
            bottom:"360px"
        },300,"linear");
    });

    /*左上动画*/
    $(".content-right-top").hover(function(){
        $(this).find(".move").animate({
            "margin-left":"-10px",
            opacity:0.6
        },300,"linear");
        $(this).find(".description").animate({
            bottom:"130px"
        },300,"linear");
        $(this).find(".advantage").animate({
            bottom:"30px",
            opacity:0.8
        },300,"linear");
    },function(){
        $(this).find(".move").animate({
            "margin-left":"0px",
            opacity:1
        },300,"linear");
        $(this).find(".advantage").animate({
            bottom:"20px",
            opacity:0
        },300,"linear");
        $(this).find(".description").animate({
            bottom:"120px"
        },300,"linear");
    });

    /*左下动画*/
    $(".content-right-bottom").hover(function(){
        $(this).find(".move").animate({
            "margin-left":"-10px",
            opacity:0.6
        },300,"linear");
        $(this).find(".description").animate({
            bottom:"320px"
        },300,"linear");
        $(this).find(".invite").animate({
            bottom:"30px",
            opacity:0.8
        },300,"linear");
    },function(){
        $(this).find(".move").animate({
            "margin-left":"0px",
            opacity:1
        },300,"linear");
        $(this).find(".invite").animate({
            bottom:"20px",
            opacity:0
        },300,"linear");
        $(this).find(".description").animate({
            bottom:"310px"
        },300,"linear");
    });
});